package com.v0lky1.smoketrainer.objects;

public class Smoke {

    private String title;
    private String url;

    public Smoke(String title, String url) {
        this.title = title;
        this.url = url;
    }

    public String getTitle() {
        return title;
    }

    public String getUrl() {
        return url;
    }
}
